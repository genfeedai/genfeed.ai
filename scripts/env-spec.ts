const KEY_TO_SECTION = new Map(
  ROOT_ENV_SECTIONS.flatMap((section, index) =>
    section.keys.map((key) => [key, { index, title: section.title }] as const),
  ),
);

export function getCanonicalEnvPath(mode: EnvMode): string {
  switch (mode) {
    case 'local':
      return '.env.local';
    case 'staging':
      return '.env.staging';
    case 'production':
      return '.env.production';
  }
}

export function getSectionForKey(key: string): {
  index: number;
  title: string;
} | null {
  return KEY_TO_SECTION.get(key) ?? null;
}
