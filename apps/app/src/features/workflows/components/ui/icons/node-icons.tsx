/**
 * Platform icon selector based on aspect ratio type
 */
export function PlatformIcon({
  type,
}: {
  type: 'phone' | 'monitor' | 'square';
}): React.JSX.Element {
  switch (type) {
    case 'phone':
      return <PhoneIcon />;
    case 'monitor':
      return <MonitorIcon />;
    case 'square':
      return <SquareIcon />;
  }
}
